package day2;

public class AssignmentOperator {

	public static void main(String[] args) {
		int num1 = 20;
		num1+=5;  //num1+5 i.e 20+5 //add the number to existing one
		System.out.println(num1);
		
		int num2 = 15;
		num2-=5;               //subtract the number from existing one
		System.out.println(num2);
		
		int num3 = 25;         //multiply the number from existing one
		num3*=2;
		System.out.println(num3);
		
		int num4 = 30;         //divide the number from existing one
		num4/=2;
		System.out.println(num4);
		
		int num5 = 25;         //performing modulus operation here.
		num5%=2;
		System.out.println(num5);
		
		
		
		

	}

}
