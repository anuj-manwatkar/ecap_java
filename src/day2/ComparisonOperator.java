package day2;

public class ComparisonOperator {

	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 20;
		
		System.out.println(num1 == num2); //compare between two number
		
		System.out.println(num1 != num2); //check the number is not equal too
		
		System.out.println(num1 > num2); //check one operand is greater than other
		
		System.out.println(num1 < num2);
		
		System.out.println(num1 >= num2); //check one operand is greater than or equal to another  //10 >= 20
		
		System.out.println(num1 <= num2); //check one operand is less than or equal to another   //10 <= 20

	}

}
